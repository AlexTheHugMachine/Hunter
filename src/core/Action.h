/** @file Action.h
 */
#ifndef ACTION_H
#define ACTION_H

/** @enum Action
 *  @brief Stores the action done by the player
 */
enum Action
{
    uparrow,    ///< The up Arrow or equivalent was pressed
    downarrow,  ///< The down Arrow or equivalent was pressed
    leftarrow,  ///< The left Arrow or equivalent was pressed
    rightarrow  ///< The right Arrow or equivalent was pressed
};

#endif